       <section class="av_textblock_section " itemscope="itemscope" itemtype="https://schema.org/CreativeWork"><div class="avia_textblock  " itemprop="text"><h4>Becas de Doctorado y Estancias cortas postdoctorales</h4>
<p align="justify">Las <strong>becas de doctorado</strong> están dirigidas a fomentar la obtención del grado académico de doctor entre los docentes de las universidades de América Latina asociadas con la FC, mientras que el objetivo de las becas de Estancias cortas postdoctorales es completar la formación postdoctoral de los profesores de estos centros y facilitar el establecimiento de redes académicas entre España y América.</p>
<p align="justify">Ambos tipos de becas se basan en un régimen de cofinanciación, fijado en convenios bilaterales de cooperación educativa, inspirado fundamentalmente en el principio de igualdad de oportunidades.</p>
<p align="justify">El marco general del sistema de cofinanciación contempla que el 60% del coste total de la beca lo cubre la Fundación Carolina y el 40% restante la universidad o institución de América Latina.</p>
<p align="justify">En un programa como éste, dirigido a fomentar el doctorado exclusivamente dentro del sistema educativo superior en América Latina, el sistema de la cofinanciación resulta muy beneficioso. La ventaja mas apreciable que aporta este programa es que hace copartícipes de estas becas a las universidades latinoamericanas para que estos docentes, una vez alcanzan el grado de doctor, retornen a sus universidades de origen que, en definitiva, son las corresponsables económicas de su formación.</p>
<p align="justify">Para la consecución de los objetivos señalados, FC negocia la firma de <strong><a title="Convenios" href="http://www.fundacioncarolina.es/formacion/becas-de-doctorado-y-estancias-cortas-postdoctorales/convenios-con-instituciones-academicas/">convenios con universidades o instancias gubernamentales</a></strong> que agrupan a todas las universidades de un mismo país o, por lo menos, a todas las que tienen la consideración de públicas. Esta última fórmula asegura la publicación de convocatorias de carácter nacional en aquellos países donde sea posible la firma de este tipo de acuerdos, con lo que se consigue que este programa resulte accesible para cualquier docente latinoamericano.</p>
<p align="justify"><strong>Solidaridad universitaria española</strong>: la mayoría de las universidades públicas españolas eximen del pago de las tasas del programa conducente al grado académico de doctor y algunas, incluso, participan en la manutención mensual de los becarios. Esta implicación supone una disminución de la aportación que corresponde a la universidad latinoamericana y a la Fundación Carolina.</p>
<p><strong>NOTA DE INTERÉS</strong></p>
<p align="justify">Fundación Universia y Fundación Carolina colaboran conjuntamente con el fin de fomentar y contribuir a hacer efectivo el principio de igualdad para las personas con discapacidad. A tal fin, Fundación Universia, en colaboración con Fundación Carolina pone en marcha un programa de ayudas complementarias para personas con discapacidad acreditada y que hayan sido beneficiarios de una beca de Fundación Carolina. El funcionamiento de estas ayudas se dará a conocer a los beneficiarios de las becas una vez las mismas hayan sido concedidas.</p>
